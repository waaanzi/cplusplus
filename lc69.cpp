/*
leetcode69. x 的平方根
实现 int sqrt(int x) 函数。
计算并返回 x 的平方根，其中 x 是非负整数。
由于返回类型是整数，结果只保留整数的部分，小数部分将被舍去。

示例 1:
输入: 4
输出: 2
示例 2:

输入: 8
输出: 2
说明: 8 的平方根是 2.82842..., 由于返回类型是整数，小数部分将被舍去。
*/
int mySqrt(int x) {
  if (0 == x) {
    return 0;
  }

  int i = 1;
  while ((i <= x) && (i <= x / i)) {
    ++i;
  }
  return i - 1;
}

int mySqrt1(int x) {
  if (0 == x) {
    return 0;
  }

  int res = 1;

  int left = 1;
  int right = x;
  while (left <= right) {
    long mid = left + (right - left) / 2;
    if (mid <= x / mid) {
      left = mid + 1;
      res = mid;
    } else {
      right = mid - 1;
    }
  }

  return res;
}

